package com.ionn;

import java.awt.*;

public class Point {
    int x;
    int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
    public void draw(Graphics g){
        g.fillOval(x - 7, y - 5, 10, 10);
    }
}
