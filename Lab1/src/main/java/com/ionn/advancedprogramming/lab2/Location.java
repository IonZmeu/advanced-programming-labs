package com.ionn.advancedprogramming.lab2;

public class Location {
    private String name;
    private float x;
    private float y;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "Location{" +
                "name='" + name + '\'' +
                ", x=" + x +
                ", y=" + y +
                '}';
    }

    enum type{
        CITIES,
        AIRPORTS,
        GAS_STATIONS
    }


}
